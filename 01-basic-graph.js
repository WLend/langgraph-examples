/**
 * 🎓 Уровень 1: Базовый граф LangGraph
 * 
 * Этот пример демонстрирует основы работы с LangGraph:
 * - Создание простого состояния
 * - Добавление одного узла
 * - Базовый переход к результату
 * 
 * Время изучения: 15-20 минут
 * Сложность: 🟢 Начальный
 */

import { StateGraph, END, START } from "@langchain/langgraph";
import { ChatPerplexity } from "@langchain/community/chat_models/perplexity";
import { ChatPromptTemplate } from "@langchain/core/prompts";
import { HumanMessage, AIMessage } from "@langchain/core/messages";
import { z } from "zod";
import dotenv from "dotenv";

// Загружаем переменные окружения
dotenv.config();

/**
 * Шаг 1: Определяем состояние графа
 * 
 * 🎯 НАЗНАЧЕНИЕ ZOD ОБЪЕКТА:
 * 
 * Zod объект (BasicGraphState) - это схема валидации и типизации для состояния графа.
 * Он выполняет несколько критически важных функций:
 * 
 * 1. 📋 ТИПИЗАЦИЯ ДАННЫХ:
 *    - Определяет структуру данных, которые передаются между узлами
 *    - Обеспечивает type safety на уровне TypeScript
 *    - Предотвращает ошибки типов во время выполнения
 * 
 * 2. 🔍 ВАЛИДАЦИЯ ВХОДНЫХ ДАННЫХ:
 *    - Проверяет корректность данных при передаче между узлами
 *    - Автоматически преобразует типы данных (например, string в number)
 *    - Выбрасывает ошибки при несоответствии схеме
 * 
 * 3. 🏗️ СТРУКТУРИРОВАНИЕ СОСТОЯНИЯ:
 *    - Создает единую точку истины для структуры данных
 *    - Обеспечивает консистентность между узлами
 *    - Упрощает отладку и мониторинг
 * 
 * 4. 🔄 СЕРИАЛИЗАЦИЯ/ДЕСЕРИАЛИЗАЦИЯ:
 *    - Позволяет сохранять состояние графа в базу данных
 *    - Восстанавливать состояние из сохраненных данных
 *    - Передавать состояние между процессами
 * 
 * 5. 📊 ДОКУМЕНТАЦИЯ:
 *    - Служит живой документацией структуры данных
 *    - Показывает разработчикам, какие поля доступны
 *    - Упрощает понимание архитектуры графа
 * 
 * БЕЗ ZOD ОБЪЕКТА:
 * - Нет гарантий типов данных
 * - Сложно отследить, какие поля доступны
 * - Высокий риск ошибок при изменении структуры
 * - Нет автоматической валидации
 * 
 * С ZOD ОБЪЕКТОМ:
 * - Полная типизация и валидация
 * - Автоматическая документация структуры
 * - Защита от ошибок типов
 * - Упрощенная отладка и разработка
 */
const BasicGraphState = z.object({
  // Входящий запрос пользователя
  userQuery: z.string().default(""),
  
  // Ответ от AI
  response: z.string().default(""),
  
  // История сообщений для контекста
  messages: z.array(z.any()).default([]),
  
  // Метаданные для отслеживания
  metadata: z.record(z.any()).default({})
});

/**
 * Шаг 2: Создаем узел обработки
 * 
 * Узел - это функция, которая принимает состояние и возвращает обновленное состояние.
 * Каждый узел должен быть асинхронной функцией.
 */
async function processUserQuery(state) {
  console.log("🔍 Обрабатываю запрос:", state.userQuery);
  
  // Создаем модель ChatPerplexity для получения актуальной информации
  const model = new ChatPerplexity({
    model: "sonar-pro",
    temperature: 0.7,
    maxTokens: 500,
    apiKey: process.env.PERPLEXITY_API_KEY,
  });
  
  // Создаем промпт для модели
  const prompt = ChatPromptTemplate.fromMessages([
    ["system", "Ты полезный AI-ассистент. Отвечай кратко и по делу на русском языке."],
    ["human", "{query}"]
  ]);
  
  try {
    // Создаем цепочку: промпт -> модель
    const chain = prompt.pipe(model);
    
    // Выполняем запрос
    const result = await chain.invoke({ 
      query: state.userQuery 
    });
    
    // Создаем сообщения для истории
    const userMessage = new HumanMessage(state.userQuery);
    const aiMessage = new AIMessage(result.content);
    
    console.log("✅ Получен ответ от AI");
    
    // Возвращаем обновленное состояние
    return {
      userQuery: state.userQuery,
      response: result.content,
      messages: [userMessage, aiMessage],
      metadata: {
        ...state.metadata,
        processedAt: new Date().toISOString(),
        model: "sonar-pro"
      }
    };
    
  } catch (error) {
    console.error("❌ Ошибка при обработке:", error.message);
    
    // В случае ошибки возвращаем состояние с сообщением об ошибке
    return {
      userQuery: state.userQuery,
      response: "Извините, произошла ошибка при обработке запроса.",
      messages: state.messages,
      metadata: {
        ...state.metadata,
        error: error.message,
        errorAt: new Date().toISOString()
      }
    };
  }
}

/**
 * Шаг 3: Создаем граф
 * 
 * 🏗️ ГРАФОВАЯ АРХИТЕКТУРА LANGGRAPH:
 * 
 * Граф в LangGraph - это структура данных, которая представляет workflow как набор узлов и рёбер:
 * 
 * 📍 УЗЛЫ (Nodes) - это действия/функции:
 *    - Каждый узел = одна функция обработки
 *    - Узел принимает состояние и возвращает обновленное состояние
 *    - Узлы выполняются последовательно или параллельно
 *    - В нашем случае: "process_query" - узел обработки запроса
 * 
 * 🔗 РЁБРА (Edges) - это переходы между узлами:
 *    - Рёбра определяют порядок выполнения узлов
 *    - Могут быть условными (зависят от состояния)
 *    - Могут быть фиксированными (всегда выполняются)
 *    - В нашем случае: START -> process_query -> END
 * 
 * 🎯 ГРАФОВЫЙ ДВИЖОК:
 *    - Управляет выполнением узлов в правильном порядке
 *    - Передает состояние между узлами
 *    - Обрабатывает ошибки и исключения
 *    - Поддерживает условную логику и циклы
 * 
 * 📊 ВИЗУАЛИЗАЦИЯ ГРАФА:
 *    START ──► [process_query] ──► END
 *              (узел обработки)
 */
const workflow = new StateGraph(BasicGraphState)
  // 🏗️ ДОБАВЛЯЕМ УЗЕЛ В ГРАФ
  // .addNode() создает узел в графе
  // "process_query" - имя узла (идентификатор)
  // processUserQuery - функция, которая будет выполняться в этом узле
  .addNode("process_query", processUserQuery)
  
  // 🔗 ДОБАВЛЯЕМ РЁБРО: START -> process_query
  // .addEdge() создает фиксированное ребро между узлами
  // START - специальный узел начала графа
  // "process_query" - целевой узел
  .addEdge(START, "process_query")

  // 🔗 ДОБАВЛЯЕМ РЁБРО: process_query -> END
  // process_query - исходный узел
  // END - специальный узел завершения графа
  .addEdge("process_query", END);

/**
 * Шаг 4: Компилируем граф
 * 
 * 🔧 КОМПИЛЯЦИЯ ГРАФА:
 * 
 * .compile() - это критически важный этап, который:
 * 
 * 1. 🏗️ СОЗДАЕТ ИСПОЛНЯЕМЫЙ ГРАФ:
 *    - Преобразует определение графа в исполняемый код
 *    - Оптимизирует порядок выполнения узлов
 *    - Создает внутренние структуры данных для управления состоянием
 * 
 * 2. 🔍 ВАЛИДАЦИЯ ГРАФА:
 *    - Проверяет, что все узлы существуют
 *    - Проверяет, что все рёбра корректны
 *    - Проверяет, что нет циклических зависимостей
 *    - Проверяет, что граф имеет начальную и конечную точки
 * 
 * 3. 🎯 СОЗДАНИЕ ГРАФОВОГО ДВИЖКА:
 *    - Создает движок, который будет выполнять узлы
 *    - Настраивает передачу состояния между узлами
 *    - Подготавливает обработку ошибок
 * 
 * 4. 📊 ОПТИМИЗАЦИЯ:
 *    - Определяет оптимальный порядок выполнения
 *    - Подготавливает параллельное выполнение (если возможно)
 *    - Создает кэш для повторных вычислений
 * 
 * БЕЗ КОМПИЛЯЦИИ:
 * - Граф нельзя выполнить
 * - Нет валидации структуры
 * - Нет оптимизации производительности
 * 
 * ПОСЛЕ КОМПИЛЯЦИИ:
 * - Получаем исполняемый объект app
 * - Можем вызывать app.invoke(initialState)
 * - Граф готов к выполнению
 */
const app = workflow.compile();

/**
 * Функция для запуска графа
 * 
 * 🚀 ВЫПОЛНЕНИЕ ГРАФА:
 * 
 * app.invoke(initialState) - это точка входа в выполнение графа:
 * 
 * 1. 🎯 ИНИЦИАЛИЗАЦИЯ ГРАФОВОГО ДВИЖКА:
 *    - Создается экземпляр графового движка
 *    - Загружается начальное состояние
 *    - Подготавливается контекст выполнения
 * 
 * 2. 🔄 ВЫПОЛНЕНИЕ УЗЛОВ ПО ПОРЯДКУ:
 *    - Движок начинает с узла START
 *    - Переходит к первому узлу согласно рёбрам
 *    - Выполняет узел и получает обновленное состояние
 *    - Переходит к следующему узлу согласно рёбрам
 *    - Повторяет до достижения узла END
 * 
 * 3. 📊 УПРАВЛЕНИЕ СОСТОЯНИЕМ:
 *    - Состояние передается между узлами
 *    - Каждый узел получает текущее состояние
 *    - Каждый узел возвращает обновленное состояние
 *    - Движок объединяет изменения состояния
 * 
 * 4. 🛡️ ОБРАБОТКА ОШИБОК:
 *    - Если узел выбрасывает ошибку, движок её перехватывает
 *    - Можно настроить fallback логику
 *    - Можно настроить retry логику
 * 
 * 5. 🎯 ЗАВЕРШЕНИЕ:
 *    - Когда достигнут узел END, выполнение завершается
 *    - Возвращается финальное состояние
 *    - Очищаются временные ресурсы
 */
async function runBasicGraph(query) {
  console.log("🚀 Запускаю базовый граф LangGraph...");
  
  // 🏗️ СОЗДАЕМ НАЧАЛЬНОЕ СОСТОЯНИЕ
  // Это состояние будет передано в первый узел графа
  // Должно соответствовать схеме BasicGraphState
  const initialState = {
    userQuery: query,
    response: "",
    messages: [],
    metadata: {
      startedAt: new Date().toISOString()
    }
  };
  
  try {
    // 🚀 ВЫПОЛНЯЕМ ГРАФ
    // app.invoke() запускает графовый движок
    // Движок выполняет узлы в порядке, определенном рёбрами
    // Возвращает финальное состояние после выполнения всех узлов
    const result = await app.invoke(initialState);
    
    console.log("✅ Граф выполнен успешно!");
    return result;
    
  } catch (error) {
    console.error("❌ Ошибка при выполнении графа:", error);
    throw error;
  }
}

/**
 * Пример использования
 */
async function main() {
  try {
    // Проверяем наличие API ключа
    if (!process.env.PERPLEXITY_API_KEY) {
      throw new Error("❌ PERPLEXITY_API_KEY не найден в переменных окружения");
    }
    
    console.log("🎓 Базовый пример LangGraph");
    console.log("=" .repeat(50));
    
    // Тестовые запросы
    const testQueries = [
      "Что такое искусственный интеллект?",
      "Расскажи о последних новостях в IT",
      "Объясни простыми словами, что такое блокчейн"
    ];
    
    for (const query of testQueries) {
      console.log(`\n📝 Запрос: ${query}`);
      console.log("⏳ Обрабатываю...");
      
      const result = await runBasicGraph(query);
      
      console.log("✅ Результат:");
      console.log("-".repeat(40));
      console.log(result.response);
      console.log("-".repeat(40));
      console.log(`📊 Метаданные:`, result.metadata);
      console.log(`💬 Сообщений: ${result.messages.length}`);
    }
    
  } catch (error) {
    console.error("❌ Критическая ошибка:", error.message);
    console.log("\n💡 Проверьте:");
    console.log("1. Создан ли файл .env с PERPLEXITY_API_KEY");
    console.log("2. Корректность API ключа");
    console.log("3. Подключение к интернету");
  }
}

// Запускаем пример, если файл выполняется напрямую
if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}

